    <script>
        $(document).ready(function() {

            $('#contactForm').validate({
                rules: {
                    firstname: {
                        required: true,
                        letterswithspaces: true
                    },
                    lastname: {
                        required: true,
                        letterswithspaces: true
                    },
                    email: {
                        required: true,
                        email: true
                    },
                    comments: {
                        required: true,
                        letterswithspaces: true
                    }
                },
                messages: {
                    firstname: {
                        required: 'Please enter your firstname.',
                        letterswithspaces: 'Name should only contain letters and spaces.'
                    },
                    lastname: {
                        required: 'Please enter your lastname.',
                        letterswithspaces: 'Name should only contain letters and spaces.'
                    },
                    email: {
                        required: 'Please enter your email address.',
                        email: 'Please enter a valid email address.'
                    },
                    comments: {
                        required: 'Please enter a message.',
                        letterswithspaces: 'Message should only contain letters and spaces.'

                    }
                },
                errorPlacement: function(error, element) {
                    error.appendTo(element.parent());
                }
            });

            $.validator.addMethod('letterswithspaces', function(value, element) {
                return this.optional(element) || /^[a-zA-Z\s]*$/.test(value);
            }, 'Please enter letters and spaces only.');

            $("#contactForm").on("submit", function(event) {
                event.preventDefault();

                if ($(this).valid()) {
                    $.ajax({
                        url: "backend.php",
                        type: "post",
                        data: $(this).serialize(),
                        beforeSend: function() {
                            $('.sub-loading img').show()
                        },
                        success: function(response) {
                            if (response.success) {
                                // alert(response.msg);
                                $('.sub-loading img').hide();
                                window.location.href = './contact-success';
                            }
                        },
                        error: function(xhr, status, error) {
                            console.error(error);
                            alert('Error: ' + error);
                        }
                    });
                } else {
                    console.log("Some fields are not validated!");
                }
            });
        });
    </script>

