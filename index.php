    <script>
        let map;
        let markerPickup;
        let markerDelivery;
        let activeMarker;

        function initMap() {
            map = new google.maps.Map(document.getElementById("map"), {
                center: {
                    lat: 13.060644638899205,
                    lng: 80.27414512422938
                },
                zoom: 5,
                mapTypeControlOptions: {
                    mapTypeIds: ['roadmap', 'satellite', 'hybrid', 'terrain', 'custom']
                }
            });
            markerDelivery = new google.maps.Marker({
                position: {
                    lat: 0,
                    lng: 0
                },
                map: map,
                draggable: true
            });

            markerPickup = new google.maps.Marker({
                position: {
                    lat: 0,
                    lng: 0
                },
                map: map,
                draggable: true
            });

            google.maps.event.addListener(map, "click", function(event) {
                activeMarker.setPosition(event.latLng);
                if (activeMarker === markerPickup) {
                    document.getElementById("pickupLocationInput").value = event.latLng.lat() + "," + event.latLng.lng();
                } else {
                    document.getElementById("deliveryLocationInput").value = event.latLng.lat() + "," + event.latLng.lng();
                }
            });
            google.maps.event.addListener(markerPickup, "click", function() {
                setActiveMarker(markerPickup);
            });
            google.maps.event.addListener(markerDelivery, "click", function() {
                setActiveMarker(markerDelivery);
            });
            google.maps.event.addListener(markerPickup, "dragend", function(event) {
                document.getElementById("pickupLocationInput").value = event.latLng.lat() + "," + event.latLng.lng();
            });
            google.maps.event.addListener(markerDelivery, "dragend", function(event) {
                document.getElementById("deliveryLocationInput").value = event.latLng.lat() + "," + event.latLng.lng();
            });
        }

        function setActiveMarker(marker) {
            activeMarker = marker;
        }

        document.getElementById("pickupLocationInput").addEventListener("click", function() {
            setActiveMarker(markerPickup);
        });

        document.getElementById("deliveryLocationInput").addEventListener("click", function() {
            setActiveMarker(markerDelivery);
        });
        document.addEventListener("DOMContentLoaded", function() {
            initMap();
        });

        function openModal() {
            let pickupLocationInput = document.getElementById("pickupLocationInput").value;
            let deliveryLocationInput = document.getElementById("deliveryLocationInput").value;

            document.getElementById("mapModal").style.display = "block";

            if (pickupLocationInput.length > 0 || deliveryLocationInput.length > 0) {
                document.getElementById("clear_btn").style.display = "block";
            } else {
                document.getElementById("clear_btn").style.display = "none";
            }
        }

        function setLocation() {
            let pickupLocationInput = document.getElementById("pickupLocationInput").value;
            let deliveryLocationInput = document.getElementById("deliveryLocationInput").value;
            if (pickupLocationInput.length > 0 || deliveryLocationInput.length > 0) {
                document.getElementById("mapModal").style.display = "none";

            } else {
                alert('Please Select Location')
            }
        }

        function closeModal() {
            document.getElementById("mapModal").style.display = "none";
        }

        function clearBtn() {
            document.getElementById("pickupLocationInput").value = "";
            document.getElementById("deliveryLocationInput").value = "";
            document.getElementById("mapModal").style.display = "none";
        }
    </script>


